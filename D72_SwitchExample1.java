package com.BasentTechnologies;

/*Java Switch statement executes one statement from multiple conditions.It is like IF-else-if ladder statement

*/
public class D72_SwitchExample1 
{  
public static void main(String[] args)
	{  
    int number=2033;  
    switch(number)
    {  
    case 10: System.out.println("10");break;  
    case 20: System.out.println("20");break;  
    case 30: System.out.println("30");break; 
    
    default:System.out.println("Not in 10, 20 or 30");  //Default code to be executed if all cases are not matched;  
  
    }
	
	
	
	}  
}  

