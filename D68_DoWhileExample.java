package com.BasentTechnologies;

/*The java do-while loop is used to iterate the part of the program several times.
If the number of iteration is not fixed and you must have to execute the loop at least once, it 
is recommended to use do-while loop.

The java do-while loops is executed at least once because the condition is checked after the loop body


*/

public class D68_DoWhileExample 
{  
	public static void main(String[] args) 
		{  			
			int i=1;
			do
			{
				System.out.println(i);
				i++;
				
			}while(i<=10);
		}  
}  
