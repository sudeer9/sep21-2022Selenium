package com.BasentTechnologies;

/* While loop is used to iterate the part of program several times.
If number of iteration is not fixed, its recommended to use while loop
*/
public class D66_WhileExample
{
	public static void main(String[] args)
	{
	 int i = 15;
	 while(i>=10)
	  {
		System.out.println(i);
		i--;
		
	  }
	}
	
	

}
