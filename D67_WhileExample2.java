//Java Infinitive While Loop
//If you pass true in the while loop, it will be infinitive while loop
public class D67_WhileExample2 
{  
	public static void main(String[] args) 
		{  
		
			while(true)
			{  
				System.out.println("infinitive while loop"); 
				//System.exit(0);
				break;
			}  
		}  
}  